"""Addition function

Contains a function that adds two numbers.
"""


def addNumbers(a: int, b: int) -> int:
    """Adds two numbers.

    Performs addition operation to two numbers.

    Args:
        a: Input to add to
        b: Input to be added

    Returns:
         Result of addition of two inputs.
    """
    return a + b


def main():
    print(addNumbers(2, 40))


if __name__ == '__main__':
    main()
